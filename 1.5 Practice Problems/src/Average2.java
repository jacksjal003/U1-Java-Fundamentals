public class Average2 {
    /*
    Given two numbers, num1 and num2, output the average.
    */

    public static void main(String[] args) {

        int num1 = 35;
        int num2 = 45;

        int sumNumber = num1 + num2;
        double average = sumNumber / 2.0;

        System.out.println("The average is " + average);


    }
}
